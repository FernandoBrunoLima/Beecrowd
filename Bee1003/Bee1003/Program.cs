// Simple Sum

namespace Bee1003_SimpleSum
{
    public class Program
    {
        public static void Main(string[] args)
        {
            int A, B, SOMA;

            A = int.Parse(Console.ReadLine());
            B = int.Parse(Console.ReadLine());

            SOMA = A + B;

            Console.WriteLine($"SOMA = {SOMA}");
        }
    }
}
